    private void create() throws SQLException {
        String nombre = TextField_nuevoNombre.getText();
        System.out.println(nombre);
        if (nombre.equals("")) {
            Label_Obligatorio.setText("*\"Nombre\" es un campo obligatorio.");
            //TextField_nuevoNombre.requestFocus();
        } else {
            String apellido = TextField_nuevoApellido.getText();
            String nacionalidad = TextField_nuevaNacionalidad.getText();

            if (conn.create(nombre, apellido, nacionalidad)) {
                JOptionPane.showMessageDialog(this, "Se insertó exitosamente");

                Label_nuevoID.setText("");
                TextField_nuevoNombre.setText("");
                TextField_nuevoApellido.setText("");
                TextField_nuevaNacionalidad.setText("");
                this.setVisible(false);
                Label_Obligatorio.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Se presentaron fallas al crear.");
            }
            }

    }

    private void update() throws SQLException {
        String nombre = TextField_nuevoNombre.getText();
        if (nombre.equals("")) {
            Label_Obligatorio.setText("*\"Nombre\" es un campo obligatorio.");
            TextField_nuevoNombre.requestFocus();
        }
        Integer id;
        id = Integer.parseInt(Label_nuevoID.getText());

        String apellido = TextField_nuevoApellido.getText();
        String nacionalidad = TextField_nuevaNacionalidad.getText();

        Author author = new Author(id, nombre, apellido, nacionalidad);
        if (conn.update(author)) {
            JOptionPane.showMessageDialog(this, "Se actualizó exitosamente");
            this.setVisible(false);
            //nuevaVentana.Label_nuevoID.setText("");
            //nuevaVentana.TextField_nuevoNombre.setText("");
            //nuevaVentana.TextField_nuevoApellido.setText("");
            //nuevaVentana.TextField_nuevaNacionalidad.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Se presentaron fallas al crear.");
        }
    }

