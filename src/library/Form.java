    public void consultar() throws SQLException {
        Integer id = null;
        if (TextField_ID.getText().length() > 0) {
            id = Integer.parseInt(TextField_ID.getText());
        }
        String nombre = null;
        if (TextField_nombre.getText().length() > 0) {
            nombre = TextField_nombre.getText();
        }
        String apellido = null;
        if (TextField_apellido.getText().length() > 0) {
            apellido = TextField_apellido.getText();
        }
        String nacionalidad = null;
        if (!"Todas".equals((String) ComboBox_Nacionalidades.getSelectedItem())) {
            nacionalidad = (String) ComboBox_Nacionalidades.getSelectedItem();
        }
        
        
        Table_autores.setModel(new javax.swing.table.DefaultTableModel(
                conn.readAuthors(id, nombre, apellido, nacionalidad),
                new String[]{"ID", "Nombre", "Apellido", "Nacionalidad"}
        ));

    }

    public void actualizarNacionalidades() throws SQLException {
        String[] nacionalidades = conn.listOfNationalities();
        String[] opciones = new String[nacionalidades.length + 1];
        opciones[0] = "Todas";
        System.arraycopy(nacionalidades, 0, opciones, 1, nacionalidades.length);
        ComboBox_Nacionalidades.setModel(new javax.swing.DefaultComboBoxModel<>( opciones ));
    }
    
    private void eliminar() throws SQLException {

        int fila = Table_autores.getSelectedRow();        
        String id = (String) Table_autores.getValueAt(fila, 0);
        String nombre = Table_autores.getValueAt(fila, 1).toString();
        String apellido = Table_autores.getValueAt(fila, 2).toString();
        String nacionalidad = Table_autores.getValueAt(fila, 3).toString(); 
        
        Object[] options = {"Sí, eliminar",
                            "No, cancelar"};
        int n = JOptionPane.showOptionDialog(this,
            "¿Desea continuar con la eliminación del registro?",
            "Eliminar",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,
            null,     //do not use a custom Icon
            options,  //the titles of buttons
            options[1]); //default button title        
        
        if( n==0 ){
            conn.delete(id, nombre, apellido, nacionalidad);
            consultar();
            ;
        }
    }
    
    
    
    
